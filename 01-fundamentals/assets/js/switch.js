const dia = 0;

switch (dia) {
    case 0:
        console.log("Domingo");
        break;
    case 1:
        console.log("Lunes");
        break;
    case 2:
        console.log("Martes");
        break;
    case 3:
        console.log("Miércoles");
        break;
    case 4:
        console.log("Jueves");
        break;
    case 5:
        console.log("Viernes");
        break;
    case 6:
        console.log("Sábado");
        break;
    default:
        console.log("Día inválido");
        break;
}
// El switch es una estructura de control que permite evaluar una expresión y ejecutar un bloque de código dependiendo del valor de esa expresión. Es útil cuando se tienen múltiples condiciones que dependen del mismo valor.